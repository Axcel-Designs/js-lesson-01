const isTrue = true;
const isFalse = false;

const and = isFalse && isTrue;
const or = isTrue || isFalse
const not = isTrue != isFalse


if (and){
    console.log(`yes`);
} else{
    console.log(`no`);
}
if (or){
    console.log(`yes`);
} else{
    console.log(`no`);
}
if (not){
    console.log(`yes`);
} else{
    console.log(`no`);
}